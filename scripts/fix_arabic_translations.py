with open(DATA_PATH) as f:
    data = json.load(f)

# Apply translations
updated = 0
for formula in data['all_formulas']:
    if formula['code'] in translations:
        t = translations[formula['code']]
        formula['name_ar'] = t['name_ar']
        formula['purpose_ar'] = t['purpose_ar']
        updated += 1

# Also update parts structure
for part in data['parts']:
    for ch in part['chapters']:
        for formula in ch['formulas']:
            if formula['code'] in translations:
                t = translations[formula['code']]
                formula['name_ar'] = t['name_ar']
                formula['purpose_ar'] = t['purpose_ar']
        for sub in ch.get('sub_sections', []):
            for formula in sub['formulas']:
                if formula['code'] in translations:
                    t = translations[formula['code']]
                    formula['name_ar'] = t['name_ar']
                    formula['purpose_ar'] = t['purpose_ar']

with open(DATA_PATH, 'w', encoding='utf-8') as f:
    json.dump(data, f, ensure_ascii=False, indent=2)

